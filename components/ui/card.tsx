import * as React from "react"

import { cn } from "@/lib/utils"

function Card({ 
  className, 
  hoverable = false,
  ...props 
}: React.ComponentProps<"div"> & { hoverable?: boolean }) {
  return (
    <div
      data-slot="card"
      className={cn(
        "glass-morphism rounded-3xl border border-primary-200/20 py-8 px-8 shadow-warm-md",
        hoverable && "transition-all duration-300 hover:translate-y-[-4px] hover:shadow-warm-lg cursor-pointer group",
        className
      )}
      {...props}
    />
  )
}

